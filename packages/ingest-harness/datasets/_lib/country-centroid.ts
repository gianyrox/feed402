export function fallbackCoord(iso2?: string): [number, number] | null {
  if (!iso2) return null;
  const c = COUNTRY_CENTROID[iso2.toUpperCase()];
  return c ?? null;
}

export function qidToIso(qidUrl?: string): string | null {
  if (!qidUrl) return null;
  const qid = qidUrl.split("/").pop()!;
  return QID_TO_ISO[qid] ?? null;
}
